// Address of the ERC721 token contract
const tokenContractAddress = "0x44f5F09Cf0BDAFBc7DA49CA6662D45FEB5C17fb2";

// React component to interact with the ERC721 token contract
function YourComponent() {
    // State variables to store web3 instance, contract instance, token ID input, and metadata
    const [web3, setWeb3] = useState(null);
    const [contract, setContract] = useState(null);
    const [tokenIdInput, setTokenIdInput] = useState('');
    const [metadata, setMetadata] = useState('');

    // Effect hook to initialize web3 when component mounts
    useEffect(() => {
        const initWeb3 = async () => {
            if (window.ethereum) {
                const _web3 = new Web3(window.ethereum);
                try {
                    // Request account access if needed
                    await window.ethereum.enable();
                    setWeb3(_web3);
                } catch (error) {
                    console.error('User denied account access');
                }
            } else if (window.web3) {
                // Legacy dapp browsers...
                const _web3 = new Web3(window.web3.currentProvider);
                setWeb3(_web3);
            } else {
                console.log('Non-Ethereum browser detected. You should consider trying MetaMask!');
            }
        };

        initWeb3();

        // Cleanup function
        return () => {
            // No cleanup needed for now
        };
    }, []);

    // Effect hook to initialize contract instance when web3 is available
    useEffect(() => {
        if (web3) {
            const _contract = new web3.eth.Contract(tokenURIABI, tokenContractAddress);
            setContract(_contract);
        }
    }, [web3]);

    // Function to handle changes in token ID input field
    const handleChange = (e) => {
        setTokenIdInput(e.target.value);
    };

    // Function to retrieve metadata for a given token ID
    const getNFTMetadata = async () => {
        if (contract && tokenIdInput !== '') {
            const result = await contract.methods.tokenURI(tokenIdInput).call();
            setMetadata(result);
        }
    };

    // JSX rendering
    return (
        <div className="container">
           <h1>TASKY</h1>
           <h3>An NFT to remember to do things...</h3>
            <input type="number" className="input-field" value={tokenIdInput} onChange={handleChange} min="0" max="4" step="1" />
            <button className="button" onClick={getNFTMetadata}>Get NFT Metadata</button>
            <p className="result">{metadata}</p>
        </div>
    );
}

export default YourComponent;
